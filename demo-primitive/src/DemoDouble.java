public class DemoDouble {
  public static void main (String args [] ){

    double d1 = 0.2; // If no notation, default it is double value
    double d10 = 0.8; // d is the notation of double
    double d2 = 0.3; 
    double d3 = d1 + d2;
    System.out.println(d3); // 0.5

    double d4 = 19999.31123912302110109;
    System.out.println(d4);// 19999.31123912302 小數點多

    float f2 = 19999.31123912302110109f;
    System.out.println(f2);// 1999.3113 小數點小
    

  }
}
